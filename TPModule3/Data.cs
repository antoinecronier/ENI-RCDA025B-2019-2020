
using System.Collections.Generic;
using System.Linq;
using TPModule3.BO;

namespace TPModule3
{
    public class Data
    {
        private static Data _instance;
        static readonly object instanceLock = new object();

        private Data()
        {
            InitialiserDatas();
        }

        public static Data Instance
        {
            get
            {
                if (_instance == null) //Les locks prennent du temps, il est préférable de vérifier d'abord la nullité de l'instance.
                {
                    lock (instanceLock)
                    {
                        if (_instance == null) //on vérifie encore, au cas où l'instance aurait été créée entretemps.
                            _instance = new Data();
                    }
                }
                return _instance;
            }
        }

        private List<Auteur> listeAuteurs = new List<Auteur>();
        private List<Livre> listeLivres = new List<Livre>();

        public List<Livre> ListeLivres
        {
            get { return listeLivres; }
            set { listeLivres = value; }
        }

        public List<Auteur> ListeAuteurs
        {
            get { return listeAuteurs; }
            set { listeAuteurs = value; }
        }

